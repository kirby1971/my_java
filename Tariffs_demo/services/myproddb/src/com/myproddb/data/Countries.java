public class Countries {

    private String countryCode;
    private String description;
    private String countryAlpha;
    private Integer countryNum;

    public String getCountryCode() {
        return countryCode;
    }

    public void setCountryCode(String countryCode) {
        this.countryCode = countryCode;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getCountryAlpha() {
        return countryAlpha;
    }

    public void setCountryAlpha(String countryAlpha) {
        this.countryAlpha = countryAlpha;
    }

    public Integer getCountryNum() {
        return countryNum;
    }

    public void setCountryNum(Integer countryNum) {
        this.countryNum = countryNum;
    }

}
